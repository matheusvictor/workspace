
public class LocalGravacao {
		
	public String getPais() {
		return pais;
	}
	public void setPais(String pais) {
		this.pais = pais;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public int getMesesDeGravacao() {
		return mesesDeGravacao;
	}
	public void setMesesDeGravacao(int mesesDeGravacao) {
		this.mesesDeGravacao = mesesDeGravacao;
	}
	private String pais;
	private String cidade;
	private int mesesDeGravacao;

}
