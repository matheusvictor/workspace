
public class Contato {
private int numero;
private String nome;
public Contato(int numero) {
	this.numero = numero;
}
public int getNumero() {
	return numero;
}
public void AlterarNumero(int numero) {
	this.numero = numero;
}
public String getNome() {
	return nome;
}
public void AlterarNome(String nome) {
	this.nome = nome;
}

}
