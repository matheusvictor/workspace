package Questao1;

public class Animal {
	
	public String Acao(){
		return açao;
	}	
	public String EmitirSom(){
		return som;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public String getSom() {
		return som;
	}
	public void setSom(String som) {
		this.som = som;
	}
	public String getAçao() {
		return açao;
	}
	public void setAçao(String açao) {
		this.açao = açao;
	}
	private String nome;
	private int idade;
	private String som;
	private String açao;

}
